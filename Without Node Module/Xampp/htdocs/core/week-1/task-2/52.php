<?php
    function test($x, $y) {
        return $x / 10 == $y / 10 || $x / 10 == $y % 10 || $x % 10 == $y / 10 || $x % 10 == $y % 10;
    }

    var_dump(test(11, 21))."<br>";
    var_dump(test(11, 20))."<br>";
    var_dump(test(10, 10))."<br>";

    //  11, 21
    // ---------
    // // -> 1 2
    // /% -> 1 1
    // %/ -> 1 2
    // %% -> 1 1

    // 11, 20
    // // -> 1 0
    // /% -> 1 0
    // %/ -> 1 0
    // %% -> 1 0

    // 10, 10
    // // -> 0 0
    // /% -> 0 0
    // %/ -> 0 0
    // %% -> 0 0
?>